char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*p;

	p = (char *)s;
	i = 0;
	while (p[i] != (char)c && p[i] != '\0')
		i++;
	if (p[i] == (char)c)
		return (&p[i]);
	return (0);
}
